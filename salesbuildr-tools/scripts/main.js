/**
 * Main JavaScript file for Salesbuildr Widget Template Tools
 * Handles common functionality across the application
 */

document.addEventListener('DOMContentLoaded', () => {
    // Tab Switching Functionality
    const tabButtons = document.querySelectorAll('.tab-button');
    const tabContents = document.querySelectorAll('.tab-content');
    
    tabButtons.forEach(button => {
        button.addEventListener('click', () => {
            // Remove active class from all buttons and contents
            tabButtons.forEach(btn => btn.classList.remove('active'));
            tabContents.forEach(content => content.classList.remove('active'));
            
            // Add active class to current button and corresponding content
            button.classList.add('active');
            const tabId = button.getAttribute('data-tab');
            document.getElementById(tabId).classList.add('active');
        });
    });
    
    // Log the app initialization
    console.log('Salesbuildr Widget Template Tools initialized');
});
